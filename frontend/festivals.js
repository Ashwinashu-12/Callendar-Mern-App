// Utility to safely build the final map
export function getFestivals() {
  const map = {};

  // Apply fixed items for each year present in YEAR_OVERRIDES
  Object.keys(YEAR_OVERRIDES).forEach((yrStr) => {
    const y = Number(yrStr);
    Object.keys(FIXED_BY_MMDD).forEach((mmdd) => {
      map[`${y}-${mmdd}`] = FIXED_BY_MMDD[mmdd];
    });
    // Year-specific overrides
    Object.assign(map, YEAR_OVERRIDES[y]);
  });

  return map;
}
